#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REGISTRY = resolve(__dirname, "..", "hooks", "corex.ts");
const OUTPUT = resolve(__dirname, "..", "hooks", "components.d.ts");

function parseHookEntries(source) {
  const importRe = /import\(\s*["']corex\/([a-z0-9-]+)["']\s*\)\s*,\s*["']([A-Za-z0-9]+)["']/g;
  const entries = [];
  let match;
  while ((match = importRe.exec(source)) !== null) {
    entries.push({ subpath: match[1], hookName: match[2] });
  }
  if (entries.length === 0) {
    throw new Error(`No hook entries found in ${REGISTRY}`);
  }
  entries.sort((a, b) => a.subpath.localeCompare(b.subpath));
  return entries;
}

function renderDts(entries) {
  const header = [
    "/**",
    " * AUTO-GENERATED by assets/scripts/gen-components-dts.mjs.",
    " * Run `npm run gen:components-dts` to refresh after editing assets/hooks/corex.ts.",
    " */",
    `type CorexHook = import("phoenix_live_view").Hook;`,
    "",
  ].join("\n");

  const blocks = entries.map(
    ({ subpath, hookName }) =>
      `declare module "corex/${subpath}" {\n  export const ${hookName}: CorexHook;\n}`
  );

  return `${header}${blocks.join("\n")}\n`;
}

function main() {
  const source = readFileSync(REGISTRY, "utf8");
  const entries = parseHookEntries(source);
  const next = renderDts(entries);
  const checkOnly = process.argv.includes("--check");

  if (checkOnly) {
    let current = "";
    try {
      current = readFileSync(OUTPUT, "utf8");
    } catch {
      // missing file  -  fall through to mismatch
    }
    if (current !== next) {
      console.error(
        `components.d.ts is out of date. Run \`npm run gen:components-dts\` to regenerate.`
      );
      process.exit(1);
    }
    return;
  }

  writeFileSync(OUTPUT, next);
  console.log(`Wrote ${OUTPUT}`);
}

main();
